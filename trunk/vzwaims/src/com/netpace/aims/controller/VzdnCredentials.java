package com.netpace.aims.controller;

public class VzdnCredentials {

	private String userName;
	private String vzdnUserType;
	private String vzdnManagerRoles;
	private String firstName;
	private String lastName;
	
	private String phoneNumber;
	private String faxNumber;
	private String mobileNumber;
	private String title;
	
	private String requestURI;
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getVzdnUserType() {
		return vzdnUserType;
	}
	public void setVzdnUserType(String vzdnUserType) {
		this.vzdnUserType = vzdnUserType;
	}
	public String getRequestURI() {
		return requestURI;
	}
	public void setRequestURI(String requestURI) {
		this.requestURI = requestURI;
	}
	public String getVzdnManagerRoles() {
		return vzdnManagerRoles;
	}
	public void setVzdnManagerRoles(String vzdnManagerRoles) {
		this.vzdnManagerRoles = vzdnManagerRoles;
	}
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getPhoneNumber() {
		return phoneNumber;
	}
	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}
	public String getFaxNumber() {
		return faxNumber;
	}
	public void setFaxNumber(String faxNumber) {
		this.faxNumber = faxNumber;
	}
	public String getMobileNumber() {
		return mobileNumber;
	}
	public void setMobileNumber(String mobileNumber) {
		this.mobileNumber = mobileNumber;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	
	
	
	
	
	
	
	
	
}
